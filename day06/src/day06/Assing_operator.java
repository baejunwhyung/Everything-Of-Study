package day06;

public class Assing_operator {

	public static void main(String[] args) {
		int a=3;
		int b=5;
		
		b=a; //b=3
		System.out.println(b);
		
		b*=a;
		System.out.println(b);
		
		a+=1; //a=1+a
		System.out.println(a);
		
		a/=2; //a=a/2
		System.out.println(a);
		
		a*=2; //a=a*2
		System.out.println(a);
		
		
	}

}
